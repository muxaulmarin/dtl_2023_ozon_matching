class ColorModel:

    def __init__(self):
        self.colors = {
            color: np.array(rgb.split(',')).astype(int)
            for color, rgb in COLORS.items()
        }
        self.d = np.sqrt(255**2 + 255**2 + 255**2)

    def distance(self, color1, color2):
        if color1 in self.colors and color2 in self.colors:
            color1 = self.colors[color1]
            color2 = self.colors[color2]
            return self._distance(color1, color2)
        else:
            return None

    def _distance(self, color1, color2):
        return np.sqrt(np.sum(np.square(color1 - color2))) / self.d

    